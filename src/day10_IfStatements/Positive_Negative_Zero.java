package day10_IfStatements;

public class Positive_Negative_Zero {
    public static void main(String[] args) {
    int number = -8;
    String message = "";
    if (number>0 ){
        message="positive";
    }else if (number <0){
         message = "negative";
    }else{
        message = "zero";
        }
        System.out.println(number +" is "+message);
















    }

}
