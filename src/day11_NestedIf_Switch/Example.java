package day11_NestedIf_Switch;

public class Example {
    public static void main(String[] args) {
        boolean X=true;
        boolean Y = !X == false;
        boolean Z = Y;

        if(X){
            System.out.println("Hello Everyone");
        }
        if(Y){
            System.out.println("Today is great day");
        }
        if(Z){
            System.out.println("We are improving everyday");
        }






    }
}
