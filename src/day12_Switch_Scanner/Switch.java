package day12_Switch_Scanner;

public class Switch {
    public static void main(String[] args) {

        switch (1){

            case 5:
                System.out.println("five");
                break;
            case 1:
                System.out.println("one");
            case 4:
                System.out.println("adadk");

            default:
                System.out.println("Invalid Case");

        }





    }
}
