package day22_NestedLoop;

public class NestedLoop3 {
    public static void main(String[] args) {

        for(int j=7; j>=1;j--){ // j:  1, 2, 3, 4, 5 ,6, 7,

            for(int k =j; k>=1; k--){
                System.out.print("*");
            }
            System.out.println();
        }
















    }
}
