package day03;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello Everyone\n\t My name is Cybertek");
        System.out.println("\t I am in Florida");
        System.out.println("\t\t\n\nI love programing");

        System.out.println("\\\\");//this prints me two back slash
        System.out.println("//");//this prints me two back slash
        System.out.println("My name is \'Nesibe\'");
        System.out.println("My name is 'Nesibe'");

        System.out.println("My favoriye TV-series: \"Game of Thrones\"");



    }



}
